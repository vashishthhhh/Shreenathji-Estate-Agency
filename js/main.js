    const validCountryCodes = ["1", "44", "91", "81", "61", "33", "49", "86", "39"]; // Add more valid country codes as needed

    document.getElementById('tel').addEventListener('input', function () {
        const input = this.value;
        const errorSpan = document.getElementById('tel-error');
        const regex = /^\+(\d{1,3})(\d{6,14})$/;
        const match = input.match(regex);
        
        if (match) {
            const countryCode = match[1];
            if (validCountryCodes.includes(countryCode)) {
                errorSpan.textContent = '';
            } else {
                errorSpan.textContent = 'Invalid country code.';
            }
        } else {
            errorSpan.textContent = 'Invalid phone number format.';
        }
    });


